void Pitu_LCDInit(void)						
{
	Pitu_LCDPutCmd(0xE2);
	OSTimeDly(4);		//Wait for 20ms
	Pitu_LCDPutCmd(0x26);	//Set Multiplex Rate and Temperature Compensation
	Pitu_LCDPutCmd(0x2f);	//Set Power Controlinternal vlcd(11x charge pump)
	Pitu_LCDPutCmd(0xea);	//set bias=1/12
	Pitu_LCDPutCmd(0x81);	//Electronic volume mode set
	Pitu_LCDPutCmd(0x72);	
						//Pitu_LCDPutCmd(0xc4);	
						//set MX=1, SEG output reverse direction		
						//set MY=0, COM output normal direction
	Pitu_LCDPutCmd(0xc8);	//set MX=0, SEG output normal direction 	
						//set MY=1, COM output reverse direction
	Pitu_LCDPutCmd(0x40);	//set start line
	Pitu_LCDPutCmd(0xAF);	//set display enable  

	

}



void Pitu_DisplayPoint(unsigned char x,unsigned char y,unsigned char point)
{
	Pitu_LCDPutCmd(176+y/8);
	Pitu_LCDPutCmd(16+x/16);
	Pitu_LCDPutCmd(x%16);
	if(point)
		Pitu_LCDPutData(0x01<<y%8);
	else
		Pitu_LCDPutData(0x00);
}

