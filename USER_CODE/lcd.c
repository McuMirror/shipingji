/*********************************************************************************************************
** Function name:		Pitu_LCDPutByte
** Descriptions:		用模拟SPI总线向LCM发送一个字节的内容
** input parameters:	dat:要发送的内容，可能是命令也可能是显示数据
** output parameters:	无
** Returned value:		无
*********************************************************************************************************/
void Pitu_LCDPutByte(unsigned char dat)
{
	unsigned char i;
	for(i=0;i<8;i++)
	{
		SCL_CLR(); 
		if(( dat & 0x80 ) == 0)
		{
			SDA_CLR();
		}
		else
		{
			SDA_SET();
		} 
		SCL_SET(); 
		dat=dat<<1;  
	}
}
/*********************************************************************************************************
** Function name:		Pitu_LCDPutCmd
** Descriptions:		向LCM写入一个字节的命令
** input parameters:	cmd:命令
** output parameters:	无
** Returned value:		无
*********************************************************************************************************/
void Pitu_LCDPutCmd(unsigned char cmd) 
{
	nCS_SET();
	CD_CLR();
	Pitu_LCDPutByte(cmd);
	CD_SET();
	nCS_CLR();
}
/*********************************************************************************************************
** Function name:		Pitu_LCDPutData
** Descriptions:		向LCM写入一个字节的命令
** input parameters:	dat:数据
** output parameters:	无
** Returned value:		无
*********************************************************************************************************/
void Pitu_LCDPutData(unsigned char dat) 
{
	nCS_SET();
	CD_SET();
	Pitu_LCDPutByte(dat);
	CD_CLR();
	nCS_CLR();
}
